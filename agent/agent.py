@agent.tool
async def search_transactions(
    ctx: RunContext[AgentDeps],
    text: str | None = None,
    category: str | None = None,
    subcategory: str | None = None,
    txn_type: str | None = None,
    min_amount: float | None = None,
    max_amount: float | None = None,
    start_date: str | None = None,
    end_date: str | None = None,
    order_by: str = "date",
    limit: int = 20,
) -> str:
    """Search individual transactions with filters; returns matching rows.

    Args:
        text: Optional case-insensitive words matched against the bank narration;
            every word must appear, in any order.
        category: Optional category name (Essentials, Lifestyle, Goals).
        subcategory: Optional subcategory name, e.g. 'Groceries', 'EMI'.
        txn_type: Optional 'credit' or 'debit'.
        min_amount: Optional lower bound on amount.
        max_amount: Optional upper bound on amount.
        start_date: ISO date YYYY-MM-DD; defaults to the start of available data.
        end_date: ISO date YYYY-MM-DD, inclusive; defaults to the end of available data.
        order_by: 'date' (newest first, default) or 'amount' (largest first).
        limit: Max rows to return (1-50, default 20).
    """
    args = dict(text=text, category=category, subcategory=subcategory,
                txn_type=txn_type, min_amount=min_amount, max_amount=max_amount,
                start_date=start_date, end_date=end_date, order_by=order_by, limit=limit)
    result = await _search_transactions(ctx.deps.user_id, **args)
    ctx.deps.citations.append(Citation(
        kind="sql_tool", ref="search_transactions",
        detail=f"{result.returned} of {result.total_matches} matching transactions",
    ))
    return await _gate(ctx, "search_transactions", args, result)


@agent.tool
async def search_finance_kb(
    ctx: RunContext[AgentDeps],
    query: str,
    limit: int = 5,
) -> str:
    """Search the personal-finance knowledge base (RBI/NCFE investor-education
    material and original articles) for concepts, rules of thumb, and how
    financial products work.

    Args:
        query: What to look up, e.g. 'emergency fund size' or '50/30/20 rule'.
        limit: Max chunks to return (1-10, default 5).
    """
    args = dict(query=query, limit=limit)
    result = await _search_finance_kb(**args)
    for hit in result.hits:
        ctx.deps.citations.append(Citation(
            kind="kb_chunk",
            ref=f"{hit.document_title} (chunk {hit.chunk_id})",
            detail=hit.page_ref or "",
        ))
    return await _gate(ctx, "search_finance_kb", args, result)


# ---------------------------------------------------------------- entry point

def _live_model():
    """Built lazily so importing this module (and offline tests) never needs
    the OpenAI key."""
    if not settings.openai_api_key:
        raise RuntimeError(
            "OPENAI_API_KEY is empty in .env; pass a test model or configure the key."
        )
    from pydantic_ai.models.openai import OpenAIChatModel
    from pydantic_ai.providers.openai import OpenAIProvider

    return OpenAIChatModel(
        settings.llm_model, provider=OpenAIProvider(api_key=settings.openai_api_key)
    )


async def run_agent(question: str, user_id: int, model=None) -> RunResult:
    """Answer one question for one user. model is injected by tests
    (TestModel/FunctionModel); None means the live configured LLM."""
    if not question.strip():
        raise ValueError("question is empty")
    if len(question) > MAX_QUESTION_CHARS:
        raise ValueError(f"question exceeds {MAX_QUESTION_CHARS} characters")
    model = model or _live_model()  # fail on a missing key before any side effects

    deps = await build_deps(user_id)
    question = await gate_text(deps.pseudonymizer, question)

    await log_payload(user_id, "agent", "system", STATIC_INSTRUCTIONS)
    await log_payload(user_id, "agent", "user", question)

    started = time.monotonic()
    result = await agent.run(
        question,
        deps=deps,
        model=model,
        usage_limits=UsageLimits(request_limit=REQUEST_LIMIT),
    )
    latency_ms = int((time.monotonic() - started) * 1000)

    answer = result.output
    await log_payload(user_id, "agent", "answer", answer, direction="from_llm")

    return RunResult(
        answer=answer,
        refused=is_refusal(answer),
        citations=deps.citations,
        tool_calls=deps.tool_calls,
        latency_ms=latency_ms,
    )
